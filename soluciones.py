def pe075(l=1500000):
    t = time()
    pp = [0] * (l + 1)
    for m in range(2, int((l // 2) ** 0.5)):
        for n in range(1 + (m & 1), m, 2):  # even's only
            if gcd(m, n) != 1: continue
            a = m ** 2 - n ** 2
            b = 2 * m * n
            c = m ** 2 + n ** 2
            p = a + b + c
            k = 1
            while p * k <= l:
                pp[p * k] += 1
                k += 1
    return pp.count(1), time() - t


